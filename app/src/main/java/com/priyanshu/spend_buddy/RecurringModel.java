package com.priyanshu.spend_buddy;

public class RecurringModel {
    String id;
    long amount;
    long day;
    String category;

    public RecurringModel(String id, long amount, long day, String category){
        this.id=id;
        this.amount=amount;
        this.day=day;
        this.category= this.category;
    }
}
